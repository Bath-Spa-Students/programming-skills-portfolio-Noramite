#this is essentially a template about telling where the cities are located in
def describe_city(city, country='Middle East'):
    print(f"{city} is located in the {country}.")

#activates the function and adds the cities and their location into the output
describe_city('Dubai', 'United Arab Emirates')
describe_city('Riyadh', 'Saudi Arabia')
describe_city('Jerusalem', 'Israel')
